2-3场过关投注，单注最高奖金限额20万元； 4-5场过关投注，单注最高奖金限额50万元； 6场及以上过关投注，单注最高奖金限额100万元。<br />
若投注中奖后，单注应兑奖金高于对应的最高奖金限额，则只兑付对应的最高限额奖金。<br />
