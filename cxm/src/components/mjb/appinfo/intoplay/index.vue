半全场：串关最高上限为4串1。<br />

<b style="margin-top:0.2rem;display:block;">三、奖金计算</b>
1.竞彩足球的过关投注奖金以出票时的赔率为准，不受投注完成后的调整变化所影响，投注页面赔率仅供参考。<br />
2.单关奖金=2.00元*出票赔率*倍数；过关奖金=2.00元*所选比赛出票赔率连乘（小数点2位）*倍数<br />
例如：购买2场比赛2串1过关玩法且中奖，出票时的赔率为1.86和2.48，倍数为10倍，过关奖金=2*1.86*2.48*10=92.2元<br />
3.奖金限额<br />
